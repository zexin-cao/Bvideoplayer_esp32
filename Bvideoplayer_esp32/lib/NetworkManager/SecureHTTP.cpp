WiFiClientSecure SecureHTTP::client;
Preferences SecureHTTP::prefs; // 初始化Preferences

bool SecureHTTP::begin() {
    client.setCACert(root_ca);
    return true;
}

bool SecureHTTP::checkAuth() {
    prefs.begin(AUTH_NAMESPACE, true);
    String token = prefs.getString("refresh_token", "");
    prefs.end();
    return !token.isEmpty();
}

String SecureHTTP::sendRequest(const String& endpoint, const String& params) {
    HTTPClient http;
    String fullUrl = String(BILI_API_ROOT) + endpoint;
    if (!params.isEmpty()) fullUrl += "?" + params;
    
    http.begin(client, fullUrl);
    int code = http.GET();
    String payload = (code == HTTP_CODE_OK) ? http.getString() : "";
    http.end();
    return payload;
}

String SecureHTTP::get(const String& url) {
    HTTPClient http;
    http.begin(client, url);
    int code = http.GET();
    String payload = (code == HTTP_CODE_OK) ? http.getString() : "";
    http.end();
    return payload;
}